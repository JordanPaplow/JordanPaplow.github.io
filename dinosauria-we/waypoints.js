var $textfade = $('.textfade');

$textfade.waypoint(function(direction) {
    if (direction == 'down') {
        $textfade.addClass('js-textfade-animate');
        document.getElementById('audio1').play();
    } else {
    $textfade.removeClass('js-textfade-animate');
    }
}, { offset:'70%'});

